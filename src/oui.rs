#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn lookup_raspberry_pi() {
        assert_eq!(lookup("B8:27:EB:12:34:56"), Some("Raspberry Pi Foundation"));
        assert_eq!(lookup("DC:A6:32:AB:CD:EF"), Some("Raspberry Pi Foundation"));
    }

    #[test]
    fn lookup_apple() {
        assert_eq!(lookup("00:03:93:AA:BB:CC"), Some("Apple"));
    }

    #[test]
    fn lookup_unknown() {
        assert_eq!(lookup("FF:FF:FF:00:00:00"), None);
    }

    #[test]
    fn lookup_case_insensitive() {
        // MAC addresses may come in lowercase from nmap
        assert_eq!(lookup("b8:27:eb:12:34:56"), Some("Raspberry Pi Foundation"));
    }

    #[test]
    fn lookup_too_short() {
        assert_eq!(lookup("B8:27"), None);
    }
}
